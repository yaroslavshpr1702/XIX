function show_events() {

    var events_elem = document.getElementById("events");
    var science_elem = document.getElementById("science");
    var leature_elem = document.getElementById("leature");
    var railroad_elem = document.getElementById("railroad");
    var people_elem = document.getElementById("people");
    events_elem.style.display = 'block';
    science_elem.style.display = 'none';
    leature_elem.style.display = 'none';
    railroad_elem.style.display = 'none';
    people_elem.style.display = 'none';
}
